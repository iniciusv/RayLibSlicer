using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Slicer3D
{
	public class Points
	{
		public float X;
		public float Y;
		public float Z;

		public Points()
		{
		}

		public Points(float x, float y, float z)
		{
			this.X = x;
			this.Y = y;
			this.Z = z;
		}
	}
}
